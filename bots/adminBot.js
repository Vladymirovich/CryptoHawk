function getServerStatus() {
  const totalMem = os.totalmem() / 1024 / 1024;
  const freeMem = os.freemem() / 1024 / 1024;
  const usedMem = totalMem - freeMem;
  const load = os.loadavg();
  const uptime = os.uptime();
  const nodeVersion = process.version;
  // –ó–¥–µ—Å—å –º–æ–∂–Ω–æ –¥–æ–±–∞–≤–∏—Ç—å –¥–æ–ø–æ–ª–Ω–∏—Ç–µ–ª—å–Ω—É—é –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏—é –ø–æ –∫–æ–Ω–Ω–µ–∫—Ç–æ—Ä–∞–º API –∏ –≤–µ–±—Ö—É–∫–∞–º, –µ—Å–ª–∏ –¥–æ—Å—Ç—É–ø–Ω–æ
  const apiStatus = "All API connectors are active.";
  const webhookStatus = "All webhooks are active.";
  
  return `üñ• **Server Status Report**:
‚Ä¢ **Node.js Version:** ${nodeVersion}
‚Ä¢ **Uptime:** ${Math.floor(uptime / 60)} minutes
‚Ä¢ **Total Memory:** ${totalMem.toFixed(2)} MB
‚Ä¢ **Used Memory:** ${usedMem.toFixed(2)} MB
‚Ä¢ **Free Memory:** ${freeMem.toFixed(2)} MB
‚Ä¢ **Load Average (1m, 5m, 15m):** ${load.map(l => l.toFixed(2)).join(', ')}

‚Ä¢ **API Status:** ${apiStatus}
‚Ä¢ **Webhook Status:** ${webhookStatus}

#CryptoHawk`;
}

